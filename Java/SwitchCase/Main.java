// package Java.SwitchCase;

public class Main {
    public static void main(String[] args) {
        char alphabet = 'g';

        switch (alphabet) {
            case 'a':
                System.out.println("Alpha");
                break;
            case 'b':
                System.out.println("Beta");
                break;
            case 'c':
                System.out.println("China");
                break;
            case 'd':
                System.out.println("Delta");
                break;
            default:
                System.out.println("Useless");
        }
    }
}
